#Escriba un programa que convierta de centímetros a pulgadas. Una pulgada es igual a 2.54 centímetros.

centimetros= float(input("Querido usuario escriba la cantidad de centimetros que desea convertir a pulgadas: "))

pulgadas=centimetros*(1.0/2.54)

print("El resultado de la conversion a pulgadas es de ", pulgadas)